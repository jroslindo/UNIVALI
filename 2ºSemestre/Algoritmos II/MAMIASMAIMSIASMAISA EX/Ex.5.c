//5 - Utilizando alocação dinâmica e aritmética de ponteiros, declare um vetor de N inteiros (onde N é um valor fornecido pelo usuário), leia um valor para cada posição e no final mostre os elementos deste vetor em posição inversa ao que foram atribuídos.///
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
#include <locale.h>

void preenche(int *vet, int N){
    int i;
	for(i=0;i<N;i++){
        printf("Digite um valor: ");
		scanf("%d",(vet+i));
	}
}

int main(){
    setlocale(LC_ALL,"Portuguese");

    int *vet, TAM, i;
    printf("Tamanho: ");
    scanf("%d",&TAM);

    vet = malloc(TAM * sizeof(int));

	preenche(vet,TAM);

    printf("\nInversa: ");
    for (i=TAM-1; i>=0; i--)
        printf("%d|",*(vet+i));

    free(vet);

    return 0;
}

